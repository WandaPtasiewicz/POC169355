package pl.calc.zad5;

 class Calculator {


     public static int add(int a, int b){
         return a+b;
     }
     public static int sub(int a, int b){
         return a-b;
     }
     public static int multi(int a, int b){
         return a*b;
     }
     public static Double divi(Double a, Double b){
         return a/b;
     }

}
