package progG.zad24;

public class Triple<T> {
    private T el1;
    private T el2;
    private T el3;

    public void setEl1(T el1) {
        this.el1 = el1;
    }

    public T getEl1() {
        return el1;
    }

    public T getEl2() {
        return el2;
    }

    public void setEl2(T el2) {
        this.el2 = el2;
    }

    public T getEl3() {
        return el3;
    }

    public void setEl3(T el3) {
        this.el3 = el3;
    }
}
